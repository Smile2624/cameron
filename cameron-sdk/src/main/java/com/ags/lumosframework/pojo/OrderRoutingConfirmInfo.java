package com.ags.lumosframework.pojo;

public class OrderRoutingConfirmInfo {

	private String operationNo;
	private String operationDesc;
	private String operationAttention;
	private String confirmBy;
	private String reConfirmBy;
	public String getOperationNo() {
		return operationNo;
	}
	public void setOperationNo(String operationNo) {
		this.operationNo = operationNo;
	}
	public String getOperationDesc() {
		return operationDesc;
	}
	public void setOperationDesc(String operationDesc) {
		this.operationDesc = operationDesc;
	}
	public String getOperationAttention() {
		return operationAttention;
	}
	public void setOperationAttention(String operationAttention) {
		this.operationAttention = operationAttention;
	}
	public String getConfirmBy() {
		return confirmBy;
	}
	public void setConfirmBy(String confirmBy) {
		this.confirmBy = confirmBy;
	}
	public String getReConfirmBy() {
		return reConfirmBy;
	}
	public void setReConfirmBy(String reConfirmBy) {
		this.reConfirmBy = reConfirmBy;
	}
	
}
